<template>
  <VExpandTransition>
    <VListItem
      ref="listItem"
      v-bind="omit(activatorProps, 'onClick')"
      :class="{ 'list-item-active': isActive }"
      :title="title"
      class="list-item"
      @click.prevent="onItemClick"
    >
      <template #prepend>
        <VBtn
          v-if="isGroup"
          :color="prependColor"
          :icon="prependIcon"
          class="ml-n1 mr-2"
          density="comfortable"
          variant="text"
          @click.stop="activatorProps?.onClick"
        />
      </template>
      <template #title>
        <div class="d-flex align-center">
          <span :class="{ 'font-weight-bold': isActive }">{{ title }}</span>
          <ActivityMenu
            v-show="isHovered"
            :activity="activity"
            activator-size="x-small"
            class="activity-menu ml-2"
            @click.stop
          />
        </div>
      </template>
      <template #append>
        <div v-if="isHovered" class="d-flex flex-column align-center">
          <VBtn
            :disabled="index === 0"
            color="primary-lighten-2"
            icon="mdi-chevron-up"
            density="compact"
            variant="text"
            size="small"
            @click.stop="reoder(Direction.Up)"
          />
          <VBtn
            :disabled="index === siblings.length - 1"
            color="primary-lighten-2"
            icon="mdi-chevron-down"
            density="compact"
            variant="text"
            size="small"
            @click.stop="reoder(Direction.Down)"
          />
        </div>
      </template>
    </VListItem>
  </VExpandTransition>
</template>

<script lang="ts" setup>
import { computed, useTemplateRef } from 'vue';
import { omit } from 'lodash-es';
import { useElementHover } from '@vueuse/core';
import type { Activity } from '@tailor-cms/interfaces/activity';

import ActivityMenu from '~/components/common/ActivityOptions/ActivityMenu.vue';

const Direction = {
  Up: -1,
  Down: 1,
};

const props = defineProps<{
  id: number;
  title: string;
  isGroup?: boolean;
  isEmpty?: boolean;
  isEditable?: boolean;
  isOpen?: boolean;
  isActive?: boolean;
  activatorProps?: Record<string, any>;
  index: number; // Location of the item in the list
  siblings: any[]; // Siblings of the item in the list
}>();

const emit = defineEmits(['edit']);

const activityStore = useActivityStore();

const listItem = useTemplateRef<HTMLButtonElement>('listItem');
const isHovered = useElementHover(listItem, {
  delayEnter: 400,
  delayLeave: 1000,
});

const activity = activityStore.findById(props.id) as Activity;

const prependIcon = computed(() => {
  if (!props.isGroup) return '';
  const icon = props.isOpen ? 'mdi-folder-open' : 'mdi-folder';
  return props.isEmpty ? `${icon}-outline` : icon;
});

const prependColor = computed(() => {
  return props.isOpen ? 'primary-lighten-3' : 'primary-lighten-2';
});

const onItemClick = (e: any) => {
  if (!props.isEditable) return props.activatorProps?.onClick(e);
  emit('edit', props.id);
};

const reoder = (direction: number) => {
  const activity = activityStore.findById(props.id) as any;
  return activityStore.reorder(activity, {
    items: props.siblings,
    position: props.index + direction,
  });
};
</script>

